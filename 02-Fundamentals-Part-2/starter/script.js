//First Challenge - Functions

const calcAverage = (a, b, c) => (a + b + c) / 3;

let dolphines = calcAverage(85, 54, 41);
let koalas = calcAverage(23, 34, 27);

const checkWinner = function (dolphinesAverage, koalasAverage) {
  if (dolphinesAverage >= 2 * koalasAverage) {
    console.log(`Dolphins win (${dolphinesAverage} vs. ${koalasAverage})`);
  } else if (koalasAverage >= 2 * dolphinesAverage) {
    console.log(`Koalas win (${koalasAverage} vs. ${dolphinesAverage})`);
  } else {
    console.log("No team wins...");
  }
};
checkWinner(dolphines, koalas);

